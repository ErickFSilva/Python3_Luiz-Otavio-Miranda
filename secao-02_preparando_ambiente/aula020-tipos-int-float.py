# Tipos int e float;

# 'int' representa qualquer número inteiro, positivo ou negativo;
print('Inteiros:')
print(11)
print(-11)
print(0)
print()

# 'float' representa qualquer número com ponto flutuante, positivo ou negativo;
print('Float:')
print(1.1, 10.11, sep=', ')
print(0.0, -1.5, sep=', ')
print()

# A função 'type' mostra o tipo que o python inferiu o valor
print(type('Erick'))
print(type(1))
print(type(1.1))
