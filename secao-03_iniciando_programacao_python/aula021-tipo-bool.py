# Tipo de dado bool (boolean)
# Ao questionar algo em um programa;
# Só existem duas respostas possíveis: sim (true) ou não (false);
# Existem vários operadores para "questionar";
# Dentre eles o "==", que é um operador lógico, questiona se um valor é igual a outro;

print(10 == 10)
print(10 == 11)

print(type(True))
print(type(False))

print(type(10 == 10))
print(type(10 == 11))
