# Tipode dados bool (boolean):
# Ao questionar algo em um programa, só existem duas respostas possíveis: true (sim) e false (não);
# Existem vários operadores para 'questionar', dentre eles, '==': operador lógico que compara se um valor é igual a outro;

print(10 == 10)
print(10 == 11)
print(type(True))
print(type(False))
print(type(10 == 10))
print(type(10 == 11))
