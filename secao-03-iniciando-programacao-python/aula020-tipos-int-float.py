"""
# Tipos INT e FLOAT

- INT: número inteiro.

O tipo int representa qualquer número positivo ou negativo.
Int sem sinal é considerado positivo.

- Float: número com ponto flutuante.

O tipo floatre presenta qualquer número positivo ou negativo com ponto flutuante.
Float sem dinal é considerado positivo.


# Função type (classe do python)

A função type mostra o tipo que o python inferiu ao valor.
"""

# Obs.: Tudo em python é um objeto.

# Tipos inteiros:
print(11)
print(-11)
print(0)

# Tipos flutuantes:
print(1.1)
print(-1.1)

# Verificando o tipo do valor informado:
print(type(11))
print(type(0))
print(type(-1.1))
print(type('Python'))
