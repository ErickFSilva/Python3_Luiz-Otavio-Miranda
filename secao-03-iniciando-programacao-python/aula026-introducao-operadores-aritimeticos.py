# - Operadores aritméticos:

# Adição
adicao = 10 + 10.2
print('Adição:', adicao)

# Subtração
subtracao = 10 - 5
print('Subtração:', subtracao)

# Multiplicação
multiplicacao= 10 * 10
print('Multiplicação:', multiplicacao)

# Divisão
divisao = 10 / 2
print('Divisão:', divisao) # Sempre retornará um float

# Divisão inteira
divisao_inteira = 10 // 2.2
print('Divisão inteira:', divisao_inteira) # Sempre retornara um valor não fracionário, int ou float

# Exponenciação
exponenciacao = 2 ** 10
print('Exponenciação:', exponenciacao)

# Módulo
modulo = 26 % 5
print('Módulo:', modulo) # Retornará o resto da divisão

print(10 % 8 == 0)
print(16 % 8 == 0)
print(10 % 2 == 0)
print(15 % 2 == 0)
print(16 % 2 == 0)
